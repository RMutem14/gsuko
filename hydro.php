                <h2>Innovation Technique : Filtration à Basse Technologie</h2>
                <p>Notre système de filtration révolutionnaire utilise des matériaux locaux pour une efficacité maximale
                    à coût minimal :</p>

                <img src="https://images.unsplash.com/photo-1605190557042-86e78967bb38?ixlib=rb-1.2.1&auto=format&fit=crop&w=1350&q=80"
